a = 14
b = 146

# IF
if b > a:
    print("1.", "b is greater than a")

# IF ELSE
if b > a:
    print("2.", "b is greater than a")
else:
    print("2.", "b is smaller or equal than a")

# IF ELIF ELSE
if b > a:
    print("3.", "b is greater than a")
elif b == a:
    print("3.", "b equal to a")
else:
    print("3.", "b is smaller than a")
