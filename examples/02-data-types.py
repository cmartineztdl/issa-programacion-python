# String
print("string", "This is a string", type("This is a string"), sep=" -> ")

# Number -> integer
print("integer", 5, type(5), sep=" -> ")

# Number -> float/decimal
print("float", 4.4, type(4.4), sep=" -> ")

# Boolean -> true/false
print("boolean", True, type(True), sep=" -> ")
print("boolean", False, type(False), sep=" -> ")

# List
print("list", [1, 2, 3], type([1, 2, 3]), sep=" -> ")

print("dict", {"a": 12}, type({"a": 12}), sep=" -> ")
